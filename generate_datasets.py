"""
This module generate some of the ERDDAP views for the ERDDAP server.

The views are generated by querying the database for the latest data and tables available
and then generating the ERDDAP views and tables based on the data. The exception to this
are the views that are templated
"""

from pathlib import Path
import re
import yaml

import click
from pathvalidate import sanitize_filename
from jinja2 import Environment, FileSystemLoader
from sqlalchemy import create_engine, text
from dotenv import load_dotenv

load_dotenv()

environment = Environment(loader=FileSystemLoader("datasets_templates"))

def generate_nature_trust_provisional_ctd_dataset():
    """
    Generate the nature trust ctd views for the ERDDAP server
    """
    # Load station configuration
    config_path = Path(
        "views/NatureTrustWaterPropertiesVerticalProfiles.yaml"
    )
    if not config_path.exists():
        click.echo(
            "Warning: NatureTrustWaterPropertiesVerticalProfiles.yaml not found, skipping stream stations view generation"
        )
        return

    with open(config_path) as f:
        config = yaml.safe_load(f)

    for nation in config.get('nations'):
        name = sanitize_filename(re.sub("[^A-Za-z0-9]", "", nation.get("nation")))
        nation["name_san"] = name
        nation["tablename"] = ("NTCTDProvisional" + name)[:63]
        result = environment.get_template(
            "NatureTrustWaterPropertiesVerticalProfilesProvisional.xml.j2"
        ).render(**nation)

        Path(
            "datasets.d/NatureTrustWaterPropertiesVerticalProfilesProvisional_%s.xml"
            % name
        ).write_text(result)
    click.echo(
        "Generated NatureTrustWaterPropertiesVerticalProfilesProvisional_%s.xml" % name
    )


